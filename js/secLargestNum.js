const getLargestNum = (type, obj) => {
  if ((type = "set")) return Math.max(...obj.values());
  if ((type = "array")) return Math.max(...obj);
};

const getPreformance = (cb, times = 1) => {
  const map = new Map();

  while (times > 0) {
    let prev = performance.now();
    cb();
    let now = performance.now();

    map.set(map.size + 1, now - prev);

    times--;
  }

  return { map, total: [...map.values()].reduce((acc, value) => acc + value) };
};

const getSecLargestNum1 = (arr) => {
  const set = new Set(arr);
  if (set.size <= 1) return "No possible Second Number";

  const firstLargestNum = getLargestNum("set", set);

  set.delete(firstLargestNum);

  return { first: firstLargestNum, second: getLargestNum("set", set) };
};

const getSecLargestNum2 = (arr) => {
  const set = new Set(arr);
  if (set.size <= 1) return "No possible Second Number";

  const newArr = [...set.values()];

  const firstIndex = newArr.indexOf(getLargestNum("array", newArr));
  const firstLargestNum = Number(newArr.splice(firstIndex, 1));

  const secondLargestNum = getLargestNum("array", newArr);

  return { first: firstLargestNum, second: secondLargestNum };
};

